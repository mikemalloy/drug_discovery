'use client'

import type { AnalyzeResponse, ApplicabilityDomain, SummarizeResponse } from '@/types/report'
import { TOXICITY_TARGETS } from '@/types/report'
import { Markdown } from '@/components/markdown'
import { Card } from '@/components/ui/card'
import { Button } from '@/components/ui/button'
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from '@/components/ui/table'
import { cn } from '@/lib/utils'
import { CheckCircle2, XCircle, AlertTriangle, Shield, ShieldAlert, ShieldCheck, Crosshair, HelpCircle, Sparkles, ArrowLeft } from 'lucide-react'

interface ResultsDisplayProps {
  data: AnalyzeResponse
  summary?: SummarizeResponse | null
  summaryLoading?: boolean
  smiles?: string
  onReset?: () => void
}

function Panel({ title, action, children, bodyClassName }: { title: React.ReactNode; action?: React.ReactNode; children: React.ReactNode; bodyClassName?: string }) {
  return (
    <Card className="gap-0 rounded-lg border py-0 shadow-none overflow-hidden">
      <div className="flex items-center justify-between gap-3 border-b border-border px-5 py-3.5">
        <span className="text-xs font-semibold uppercase tracking-wide text-foreground">{title}</span>
        {action}
      </div>
      <div className={cn('p-5', bodyClassName)}>{children}</div>
    </Card>
  )
}

function RiskBadge({ tier }: { tier: 'Low' | 'Moderate' | 'High' }) {
  const config = {
    Low: { label:'Low Risk', className:'bg-success/10 text-success', icon: ShieldCheck },
    Moderate: { label:'Moderate Risk', className:'bg-warning/10 text-warning', icon: Shield },
    High: { label:'High Risk', className:'bg-accent/10 text-accent', icon: ShieldAlert },
  }
  const { className, label, icon: Icon } = config[tier]
  return <span className={cn('inline-flex items-center gap-2 px-4 py-2 text-sm font-semibold rounded-md', className)}><Icon className="h-4 w-4" />{label}</span>
}

function PassFail({ pass }: { pass: boolean }) {
  return pass
    ? <span className="inline-flex items-center gap-1.5 text-success text-sm font-medium"><CheckCircle2 className="h-4 w-4" />Pass</span>
    : <span className="inline-flex items-center gap-1.5 text-accent text-sm font-medium"><XCircle className="h-4 w-4" />Fail</span>
}

function AiBody({ summary, loading }: { summary?: SummarizeResponse | null; loading?: boolean }) {
  if (loading) return <div className="flex items-center gap-3 text-sm text-muted-foreground"><span className="inline-block h-3 w-3 rounded-full bg-accent/60 animate-pulse" />Generating grounded summary…</div>
  if (!summary || !summary.available) return <p className="text-sm text-muted-foreground">Summary unavailable{summary?.reason ? ` (${summary.reason})` : ''}. The analysis below is unaffected.</p>
  return (
    <>
      <Markdown>{summary.markdown ?? ''}</Markdown>
      {summary.governance && (
        <details className="mt-5 pt-4 border-t border-border/50">
          <summary className="text-xs text-muted-foreground cursor-pointer select-none hover:text-foreground">Generated by {summary.governance.model} · click for audit metadata</summary>
          <dl className="mt-3 grid grid-cols-[auto_1fr] gap-x-4 gap-y-1 text-xs font-mono text-muted-foreground">
            <dt>generated_at</dt><dd className="break-all">{summary.governance.generated_at}</dd>
            <dt>temperature</dt><dd>{summary.governance.temperature}</dd>
            <dt>prompt_sha256</dt><dd className="break-all">{summary.governance.prompt_sha256}</dd>
            <dt>input_sha256</dt><dd className="break-all">{summary.governance.input_report_sha256}</dd>
          </dl>
          <p className="mt-3 text-xs text-muted-foreground leading-relaxed">Interpretation of model outputs only. Not a clinical, dosing, or fitness-for-use recommendation.</p>
        </details>
      )}
    </>
  )
}

function ADPanel({ ad }: { ad?: ApplicabilityDomain }) {
  if (!ad || !ad.available) return (
    <Panel title="Applicability Domain">
      <div className="flex items-start gap-3 bg-muted/40 border border-border p-4 rounded-md">
        <HelpCircle className="h-4 w-4 text-muted-foreground mt-0.5 shrink-0" />
        <p className="text-sm text-muted-foreground">Reliability check unavailable{ad?.reason ? ` (${ad.reason})` : ''}. The prediction is shown without an in-domain assessment.</p>
      </div>
    </Panel>
  )
  const r = ad.reliability ?? 'low'
  const cfg = {
    high: { label:'High Reliability', blurb:'This molecule closely resembles the training set — the prediction is well within the model’s applicability domain.', className:'bg-success/10 text-success', icon: ShieldCheck },
    moderate: { label:'Moderate Reliability', blurb:'This molecule is inside the applicability domain but near its edge — treat the prediction with some caution.', className:'bg-warning/10 text-warning', icon: Shield },
    low: { label:'Out of Domain', blurb:'This molecule is unlike anything the model was trained on. The prediction is an extrapolation and should not be trusted.', className:'bg-accent/10 text-accent', icon: ShieldAlert },
    invalid: { label:'Unassessable', blurb:'The structure could not be assessed against the training set.', className:'bg-muted text-muted-foreground', icon: HelpCircle },
  }[r]
  const Icon = cfg.icon
  const pct = (v?: number) => v === undefined ? '—' : `${(v * 100).toFixed(1)}%`
  return (
    <Panel title="Applicability Domain">
      <div className="grid gap-8 lg:grid-cols-[320px_1fr] items-start">
        <div>
          <span className={cn('inline-flex items-center gap-2 px-4 py-2 text-sm font-semibold rounded-md', cfg.className)}><Icon className="h-4 w-4" />{cfg.label}</span>
          <p className="mt-4 text-sm text-muted-foreground leading-relaxed">{cfg.blurb}</p>
          <div className="mt-6">
            {[['Nearest-neighbor similarity', pct(ad.max_similarity)], [`Mean of top ${ad.k ?? 5} similarity`, pct(ad.mean_top_k_similarity)], ['In-domain threshold', pct(ad.threshold)]].map(([label, value], i, arr) => (
              <div key={label} className={cn('flex items-center justify-between py-3', i < arr.length - 1 && 'border-b border-border/50')}>
                <span className="text-sm text-muted-foreground">{label}</span>
                <span className="text-sm font-mono font-medium text-foreground">{value}</span>
              </div>
            ))}
          </div>
        </div>
        {ad.nearest_neighbors && ad.nearest_neighbors.length > 0 && (
          <div>
            <div className="flex items-center gap-2 mb-4"><Crosshair className="h-3.5 w-3.5 text-muted-foreground" /><span className="text-xs font-medium uppercase tracking-wide text-muted-foreground">Most similar training molecules</span></div>
            <div className="border border-border overflow-hidden rounded-md">
              <Table>
                <TableHeader><TableRow className="bg-muted/50 hover:bg-muted/50 border-b border-border"><TableHead className="text-xs font-medium uppercase tracking-wide text-muted-foreground h-11">SMILES</TableHead><TableHead className="text-xs font-medium uppercase tracking-wide text-muted-foreground text-right h-11">Similarity</TableHead></TableRow></TableHeader>
                <TableBody>{ad.nearest_neighbors.map((n, i) => (<TableRow key={`${n.smiles}-${i}`} className="border-b border-border/50 last:border-0 hover:bg-transparent"><TableCell className="text-xs font-mono py-3 break-all max-w-[320px]">{n.smiles}</TableCell><TableCell className="text-sm text-right font-mono py-3 text-muted-foreground">{(n.similarity * 100).toFixed(1)}%</TableCell></TableRow>))}</TableBody>
              </Table>
            </div>
          </div>
        )}
      </div>
    </Panel>
  )
}

export function ResultsDisplay({ data, summary, summaryLoading, smiles, onReset }: ResultsDisplayProps) {
  const toxicCount = Object.values(data.toxicity).filter(t => t.label?.toLowerCase() === 'toxic').length
  const safeCount = Object.values(data.toxicity).filter(t => t.label?.toLowerCase() === 'safe').length
  const safePct = (safeCount + toxicCount) ? Math.round((safeCount / (safeCount + toxicCount)) * 100) : 0

  return (
    <div className="space-y-6">
      <div className="flex flex-wrap items-end justify-between gap-6 border-b border-border pb-7">
        <div>
          <div className="flex items-center gap-3"><div className="w-8 h-0.5 bg-accent" /><span className="text-xs font-medium tracking-widest uppercase text-accent">Analysis Results</span></div>
          <div className="mt-4 flex flex-wrap items-center gap-4">
            {data.compound_name && <h1 className="text-3xl font-bold tracking-tight text-foreground">{data.compound_name}</h1>}
            {smiles && <code className="font-mono text-sm text-muted-foreground bg-muted border border-border rounded-md px-2.5 py-1 break-all">{smiles}</code>}
          </div>
        </div>
        {onReset && <Button variant="outline" onClick={onReset}><ArrowLeft className="mr-2 h-4 w-4" />New analysis</Button>}
      </div>
      <div className="grid gap-4 sm:grid-cols-3">
        <Panel title="Risk Assessment"><div className="flex h-full items-center"><RiskBadge tier={data.risk_summary.tier} /></div></Panel>
        <Panel title="Composite Score"><div className="flex items-baseline gap-2"><span className="text-4xl font-bold tabular-nums text-foreground leading-none">{data.risk_summary.composite_score.toFixed(2)}</span><span className="font-mono text-sm text-muted-foreground">/ 1.00</span></div></Panel>
        <Panel title="Endpoint Summary">
          <div className="mb-2.5 flex items-center justify-between whitespace-nowrap"><span className="text-sm font-medium"><span className="text-success">{safeCount}</span> Safe</span><span className="text-sm font-medium"><span className="text-accent">{toxicCount}</span> Toxic</span></div>
          <div className="h-1.5 overflow-hidden rounded-full bg-accent/10"><div className="h-full bg-success" style={{ width: `${safePct}%` }} /></div>
        </Panel>
      </div>
      <div className="grid gap-6 items-start lg:grid-cols-[1.55fr_1fr]">
        <div className="space-y-6">
          <Panel title={<span className="flex items-center gap-2"><Sparkles className="h-3.5 w-3.5 text-accent" /> AI Summary</span>}><AiBody summary={summary} loading={summaryLoading} /></Panel>
          <Panel title="Tox21 Toxicity Endpoints" action={toxicCount > 0 ? <span className="inline-flex items-center gap-1.5 text-xs font-medium text-accent"><AlertTriangle className="h-3.5 w-3.5" />{toxicCount} flagged</span> : undefined} bodyClassName="p-0">
            <Table>
              <TableHeader><TableRow className="bg-muted/50 hover:bg-muted/50 border-b border-border"><TableHead className="text-xs font-medium uppercase tracking-wide text-muted-foreground h-11 pl-5">Target</TableHead><TableHead className="text-xs font-medium uppercase tracking-wide text-muted-foreground text-right h-11">Probability</TableHead><TableHead className="text-xs font-medium uppercase tracking-wide text-muted-foreground text-right h-11 pr-5">Result</TableHead></TableRow></TableHeader>
              <TableBody>
                {TOXICITY_TARGETS.map(target => {
                  const r = data.toxicity[target]; if (!r) return null
                  const toxic = r.label?.toLowerCase() === 'toxic'
                  return (
                    <TableRow key={target} className={cn('border-b border-border/50 last:border-0 hover:bg-transparent', toxic && 'bg-accent/5')}>
                      <TableCell className="text-sm font-mono py-3 pl-5">{target}</TableCell>
                      <TableCell className="text-sm text-right font-mono py-3 text-muted-foreground">{(r.probability * 100).toFixed(1)}%</TableCell>
                      <TableCell className="text-sm text-right py-3 pr-5">{toxic ? <span className="inline-flex items-center gap-1.5 text-accent font-medium"><XCircle className="h-3.5 w-3.5" />Toxic</span> : <span className="inline-flex items-center gap-1.5 text-success font-medium"><CheckCircle2 className="h-3.5 w-3.5" />Safe</span>}</TableCell>
                    </TableRow>
                  )
                })}
              </TableBody>
            </Table>
          </Panel>
        </div>
        <div className="space-y-6">
          <Panel title="Molecular Structure" bodyClassName="p-0">
            <div className="flex min-h-[240px] items-center justify-center p-6"><div className="molecule-svg-container max-w-full" dangerouslySetInnerHTML={{ __html: data.structure_svg }} /></div>
            {data.compound_name && <p className="px-5 pb-4 text-sm text-muted-foreground text-center font-medium">{data.compound_name}</p>}
          </Panel>
          <Panel title="ADMET Profile">
            <div className="space-y-0">
              {[['Molecular Weight', `${data.admet.molecular_weight.toFixed(2)} Da`],['LogP', data.admet.logp.toFixed(2)],['H-Bond Donors', data.admet.hbd],['H-Bond Acceptors', data.admet.hba],['TPSA', `${data.admet.tpsa.toFixed(2)} Å²`],['Rotatable Bonds', data.admet.rotatable_bonds]].map(([label, value]) => (
                <div key={String(label)} className="flex items-center justify-between py-3 border-b border-border/50"><span className="text-sm text-muted-foreground">{label}</span><span className="text-sm font-mono font-medium text-foreground">{value}</span></div>
              ))}
              <div className="flex items-center justify-between py-3 border-b border-border/50"><span className="text-sm text-muted-foreground">Lipinski Rule of 5</span><PassFail pass={data.admet.lipinski_pass} /></div>
              <div className="flex items-center justify-between py-3 border-b border-border/50"><span className="text-sm text-muted-foreground">Veber Rules</span><PassFail pass={data.admet.veber_pass} /></div>
              <div className="flex items-center justify-between py-3"><span className="text-sm text-muted-foreground">PAINS Alerts</span>{data.admet.pains_alerts.length === 0 ? <span className="text-sm text-success font-medium">None</span> : <span className="text-sm text-accent font-medium">{data.admet.pains_alerts.join(', ')}</span>}</div>
            </div>
          </Panel>
        </div>
      </div>
      <ADPanel ad={data.applicability_domain} />
    </div>
  )
}
